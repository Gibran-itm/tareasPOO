public class Rectangulo {

    // double
    public static double Area (double lado1, double lado2) {
        return lado1 * lado2;
    }
    public static double Perimetro (double lado1, double lado2) {
        return lado1 + lado1 + lado2 + lado2;
    }
    // int
    public static int Area (int lado1, int lado2) {
        return lado1 * lado2;
    }
    public static int Perimetro (int lado1, int lado2) {
        return lado1 + lado1 + lado2 + lado2;
    }



}
